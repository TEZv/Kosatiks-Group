const state = {
  lang: localStorage.getItem("kosatiks-lang") || "ua",
  roleFilter: "all",
  typeFilter: "all"
};

function t(key) {
  return translations[state.lang][key] || translations.en[key] || key;
}

function applyTranslations() {
  document.documentElement.lang = state.lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = t(key);
  });
}

function renderRoleCards() {
  const container = document.getElementById("roleGrid");
  container.innerHTML = roleCards
    .map((card) => {
      const bullets = card.bullets[state.lang].map((item) => `<li>${item}</li>`).join("");
      return `
        <article class="role-card">
          <h3>${card.title[state.lang]}</h3>
          <p>${card.body[state.lang]}</p>
          <ul>${bullets}</ul>
        </article>
      `;
    })
    .join("");
}

function renderFilterGroup(containerId, items, currentValue, handlerName) {
  const container = document.getElementById(containerId);
  container.innerHTML = items
    .map(
      (item) => `
        <button type="button" class="filter-chip ${item.value === currentValue ? "is-active" : ""}" data-${handlerName}="${item.value}">
          ${item.label}
        </button>
      `
    )
    .join("");
}

function renderFilters() {
  renderFilterGroup("roleFilters", [
    { value: "all", label: t("filter.all") },
    { value: "strategist", label: t("filter.strategist") },
    { value: "producer", label: t("filter.producer") },
    { value: "creator", label: t("filter.creator") }
  ], state.roleFilter, "role");

  renderFilterGroup("typeFilters", [
    { value: "all", label: t("filter.all") },
    { value: "self", label: t("filter.self") },
    { value: "hire", label: t("filter.hire") },
    { value: "project", label: t("filter.project") },
    { value: "research", label: t("filter.research") }
  ], state.typeFilter, "type");
}

function getFilteredCards() {
  return workCards.filter((card) => {
    const roleOk = state.roleFilter === "all" || card.role === state.roleFilter;
    const typeOk = state.typeFilter === "all" || card.type === state.typeFilter;
    return roleOk && typeOk;
  });
}

function actionLinks(card) {
  const links = [];
  if (card.repo) {
    links.push(`<a href="${card.repo}" target="_blank" rel="noreferrer">${t("card.repo")}</a>`);
  }
  if (card.live) {
    links.push(`<a href="${card.live}" target="_blank" rel="noreferrer">${t("card.live")}</a>`);
  }
  if (card.org) {
    links.push(`<a href="${card.org}" target="_blank" rel="noreferrer">${t("card.org")}</a>`);
  }
  if (!links.length) {
    links.push(`<span class="meta-pill">${t("card.comingSoon")}</span>`);
  }
  return links.join("");
}

function renderCards() {
  const container = document.getElementById("cardGrid");
  const cards = getFilteredCards();
  container.innerHTML = cards.map((card) => {
    const tags = card.tags.map((tag) => `<span class="tag role-${card.role}">${tag}</span>`).join("");
    return `
      <article class="work-card">
        <div class="card-meta">
          <span class="meta-pill">${t(`filter.${card.role}`)}</span>
          <span class="meta-pill">${t(`filter.${card.type}`)}</span>
          <span class="meta-pill">${t(`status.${card.status}`)}</span>
        </div>
        <div>
          <h3>${card.title[state.lang]}</h3>
          <p>${card.body[state.lang]}</p>
        </div>
        <div class="tag-row">${tags}</div>
        <div class="card-actions">${actionLinks(card)}</div>
      </article>
    `;
  }).join("");
}

function bindEvents() {
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      state.lang = button.dataset.lang;
      localStorage.setItem("kosatiks-lang", state.lang);
      document.querySelectorAll("[data-lang]").forEach((node) => {
        node.classList.toggle("is-active", node.dataset.lang === state.lang);
      });
      renderAll();
    });
  });

  document.addEventListener("click", (event) => {
    const role = event.target.closest("[data-role]");
    if (role) {
      state.roleFilter = role.dataset.role;
      renderAll();
    }

    const type = event.target.closest("[data-type]");
    if (type) {
      state.typeFilter = type.dataset.type;
      renderAll();
    }
  });
}

function renderAll() {
  applyTranslations();
  renderRoleCards();
  renderFilters();
  renderCards();
}

bindEvents();
renderAll();
